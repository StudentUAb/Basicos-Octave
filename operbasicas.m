% Exemplo de operações matemáticas básicas com variáveis no Octave
% Defina algumas variáveis
a = 6;
b = 3;
% Adição
soma = a + b;
disp(['Soma: ', num2str(soma)]);
% Subtração
subtracao = a - b;
disp(['Subtracao: ', num2str(subtracao)]);
% Multiplicação
multiplicacao = a * b;
disp(['Multiplicacao: ', num2str(multiplicacao)]);
% Divisão
divisao = a / b;
disp(['Divisao: ', num2str(divisao)])